// Enhanced sliding animations for sections
const sectionObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            const section = entry.target;
            const animatedElements = section.querySelectorAll('.animate-on-scroll');
            
            animatedElements.forEach((element, index) => {
                setTimeout(() => {
                    element.classList.add('animated');
                }, index * 200); // Stagger the animations
            });
        }
    });
}, { threshold: 0.3 });

// Observe sections for staggered animations
document.addEventListener('DOMContentLoaded', () => {
    const sections = document.querySelectorAll('section');
    sections.forEach(section => {
        sectionObserver.observe(section);
    });
});

// Parallax sliding effect for hero section
window.addEventListener('scroll', () => {
    const scrolled = window.pageYOffset;
    const hero = document.querySelector('.hero');
    const heroContent = document.querySelector('.hero-content');
    const heroImage = document.querySelector('.hero-image');
    
    if (hero && scrolled < hero.offsetHeight) {
        const rate = scrolled * 0.5;
        const rate2 = scrolled * 0.3;
        
        if (heroContent) {
            heroContent.style.transform = `translateX(${rate}px)`;
        }
        
        if (heroImage) {
            heroImage.style.transform = `translateX(-${rate2}px)`;
        }
    }
});

// Smooth sliding navigation
document.querySelectorAll('.nav-link').forEach(link => {
    link.addEventListener('click', function(e) {
        e.preventDefault();
        const targetId = this.getAttribute('href');
        const targetSection = document.querySelector(targetId);
        
        if (targetSection) {
            // Add sliding effect to the scroll
            targetSection.scrollIntoView({
                behavior: 'smooth',
                block: 'start'
            });
            
            // Add a brief sliding animation to the target section
            setTimeout(() => {
                targetSection.style.animation = 'slideInFromTop 0.5s ease-out';
                setTimeout(() => {
                    targetSection.style.animation = '';
                }, 500);
            }, 300);
        }
    });
});

